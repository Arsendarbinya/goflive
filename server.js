function createObjects() {
    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 1) {
                var newGrass = new Grass1(x, y, 1);
                grassArr.push(newGrass);
            } else if (matrix[y][x] == 2) {
                var New = new GrassEater1(x, y, 2);
                grassEaterArr.push(New);
            }else if(matrix[y][x]==3){
                var New = new black(x, y, 3,6,11);
                blackArr.push(New)
            }
            else if(matrix[y][x]==4){
                var New = new white(x, y, 4,30);
                whiteArr.push(New)
            }
            else if(matrix[y][x]==5){
                var New = new blackspawner(x, y, 5);
                spawnerArr.push(New)
            }

        }
    }
    io.sockets.emit('send matrix', matrix)
    console.log(matrix.length)
    console.log(matrix[0].length)

}




function game() {
    for (var i in grassArr) {
        grassArr[i].mull(5, 0);
    }
    for (var i in grassEaterArr) {
        grassEaterArr[i].eat();
    }
    for (var i in blackArr) {
        blackArr[i].mull();
    }

    for (var i in spawnerArr) {
        if (spawnerArr[i].SpawnerTime < 25) {
            spawnerArr[i].move()
        } else {
            spawnerArr[i].Stop()
        }
    }
    if(timer<40){
    	
	    for (var i in whiteArr) {
	        whiteArr[i].mull();
			if(whiteArr[i].move()==true){
				var g =2
			}else{
				whiteArr[i].die(0);
			}
	    }
	}else{
		for (var i in whiteArr) {
	    	matrix[whiteArr[i].y][whiteArr[i].x] = 0;
	    	whiteArr.splice(i,1);
	    	timer=0
	    }				
		
    }
    io.sockets.emit("send matrix", matrix);
}

